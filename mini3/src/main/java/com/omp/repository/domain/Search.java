package com.omp.repository.domain;

public class Search {

	private String val;
	private String mnf;
	private String year;
	public String getVal() {
		return val;
	}
	public void setVal(String val) {
		this.val = val;
	}
	public String getMnf() {
		return mnf;
	}
	public void setMnf(String mnf) {
		this.mnf = mnf;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	
	
}
