package com.omp.repository.domain;

public class Opt {

	private int optionNo;
	private int optionGroupNo;
	private String val;
	public int getOptionNo() {
		return optionNo;
	}
	public void setOptionNo(int optionNo) {
		this.optionNo = optionNo;
	}
	public int getOptionGroupNo() {
		return optionGroupNo;
	}
	public void setOptionGroupNo(int optionGroupNo) {
		this.optionGroupNo = optionGroupNo;
	}
	public String getVal() {
		return val;
	}
	public void setVal(String val) {
		this.val = val;
	}
	
	
}
